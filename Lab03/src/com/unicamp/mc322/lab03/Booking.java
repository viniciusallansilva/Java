	public static void criaReservas(User cliente, Hotel hotel,int numeroQuarto, int numeroDias) {
		if(hotel.verificaDisponibilidade(numeroQuarto)==false || (cliente.getFumante()==true && hotel.retornaQuarto(numeroQuarto).getFumante()==false)){
			System.out.println("Impossivel realizar Reserva\n");
		}
		else {
			float precoHospedagem;
			if(hotel.retornaQuarto(numeroQuarto).getEhVip()==true) {
				precoHospedagem=hotel.getPrecoVip()*numeroDias;
			}
			else {
				precoHospedagem=hotel.getPrecoNormal()*numeroDias;
			}
			if(cliente.getSaldo()<precoHospedagem) {
				System.out.println("Saldo insuficiente\n");
			}
			else {
				System.out.println("Reserva efetuada com sucesso, valor: R$"+precoHospedagem+"\n");
				hotel.retornaQuarto(numeroQuarto).setOcupacaoQuarto(true);
				cliente.setSaldo(cliente.getSaldo()-precoHospedagem);
				cliente.setValorPago(precoHospedagem);
			}
		}
	}
	
	public static void cancelaReserva(User cliente, Hotel hotel, int numeroQuarto) {
		if(hotel.verificaDisponibilidade(numeroQuarto)==true) {
			hotel.retornaQuarto(numeroQuarto).setOcupacaoQuarto(false);
			System.out.println("Desculpa pelo incomodo\n");
			cliente.setSaldo((float)0.7*cliente.getValorPago());
		}
		else {
			System.out.println("O quarto já se encontra disponivel\n");
		}
	}

